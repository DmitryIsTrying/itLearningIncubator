class MiniStringFuck {
  constructor(code) {
    this.code = code
  }

  static decrypt(code) {
    let currentChar = 0
    let result = ''

    code = code.replace(/[^+.]/g, '')

    code.replace(/([+]+|[.])/g, (chars) => {
      if (chars.includes('+')) {
        currentChar = (currentChar + chars.length) % 256
      }
      if (chars.includes('.')) {
        result += String.fromCharCode(currentChar)
      }
    })

    return result
  }
}

function myFirstInterpreter(code) {
  return MiniStringFuck.decrypt(code)
}

